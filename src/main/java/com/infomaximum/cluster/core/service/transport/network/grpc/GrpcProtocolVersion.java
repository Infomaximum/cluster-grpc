package com.infomaximum.cluster.core.service.transport.network.grpc;

/**
 * Версия транспортного протокола cluster-grpc.
 * Передаётся в handshake-сообщении при подключении нод и сравнивается обеими сторонами:
 * при несовпадении значений соединение разрывается до начала обмена прикладными пакетами.
 */
public final class GrpcProtocolVersion {

    /**
     * Текущая версия протокола.
     * Поднимать значение на единицу при любом из следующих изменений:
     * <ul>
     *     <li>добавление нового {@code oneof}-варианта в сообщение {@code PNetPackage};</li>
     *     <li>добавление, удаление или изменение поля любого из вложенных сообщений {@code PNetPackage*};</li>
     *     <li>изменение семантики обработки или интерпретации существующего поля.</li>
     * </ul>
     * Без бампа значения старая нода может успешно пройти handshake с новой и затем упасть
     * на пакете незнакомого формата.
     */
    public static final int CURRENT = 1;

    private GrpcProtocolVersion() {
    }
}
